void printQueue(queue<int>&Queue)
{
    while(!Queue.empty())
    {
        cout<<Queue.front()<<" ";
        Queue.pop();
    }
}

void reverseQueue(queue<int>&Queue)
{
    stack<int>st;
    while(!Queue.empty())
    {
        st.push(Queue.front());
        Queue.pop();
    }

    while(!st.empty())
    {
        Queue.push(st.top());
        st.pop();
    }
}
int main() {
    queue<int>q;
    q.push(2);
    q.push(9);
    q.push(7);
    q.push(4);

    reverseQueue(q);
    printQueue(q);

}



