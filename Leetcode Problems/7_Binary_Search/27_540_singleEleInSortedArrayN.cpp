#include <iostream>
#include <vector>
#include <string>
#include <unordered_map>
#include <unordered_set>
#include <stack>
#include <algorithm>
#include <climits>
using namespace std;

class Solution
{
public:
    int singleNonDuplicate(vector<int> &nums)
    {
        int n = nums.size();

        // If only one element
        if (n == 1)
            return nums[0];

        // Check first element
        if (nums[0] != nums[1])
            return nums[0];

        // Check last element
        if (nums[n - 1] != nums[n - 2])
            return nums[n - 1];

        // Check middle part
        for (int i = 1; i < n - 1; i++)
        {
            if (nums[i] != nums[i - 1] && nums[i] != nums[i + 1])
            {
                return nums[i];
            }
        }

        return -1; // never happens
    }
};

int main()
{
    Solution sol;
    vector<int> v = {1, 1, 2, 3, 3, 4, 4, 8, 8};
    cout << sol.singleNonDuplicate(v);
    return 0;
}

